     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        // Validation des données utilisateur
        $request->validate([
            'name' => 'required|string|max:255',
        ]);

        // Création de l'utilisateur sans rôle (role_id sera null au départ)
